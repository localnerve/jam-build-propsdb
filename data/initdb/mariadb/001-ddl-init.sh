#!/bin/sh

SQL="$(cat <<EOF
CREATE DATABASE IF NOT EXISTS $AUTHZ_DATABASE;
CREATE DATABASE IF NOT EXISTS $DB_APP_DATABASE;
CREATE USER IF NOT EXISTS '$DB_USER'@'%' IDENTIFIED BY '$DB_PASSWORD';
CREATE USER IF NOT EXISTS '$DB_APP_USER'@'%' IDENTIFIED BY '$DB_APP_PASSWORD';
CREATE TABLE IF NOT EXISTS $AUTHZ_DATABASE.authorizer_users (id CHAR(36) NOT NULL PRIMARY KEY);
EOF
)"

echo "$SQL" | mariadb -u root --password="$DB_ROOT_PASSWORD" 